import math as m

#ceil() 
print(m.ceil(9.35)) # to round a number including approx #10
#floor()-to print largest number
print(m.floor(-25.98)) #-26
print(m.floor(25.98)) #25
print(m.floor(70.15)) #70
#pow()
print(m.pow(2,2)) #4.0
#fabs()-to return absolute value of a number
print(m.fabs(-98)) #98
#factorial()
print(m.factorial(7)) #5040
print(m.factorial(2)) #2
#sin()
print(m.sin(10)) #-0.544021110889
print(m.sin(m.radians(30))) #0.4999999999999994
#cos()
print(m.cos(10)) #-0.830971529076
print(m.cos(m.radians(30))) #0.8660254037844387
#tan()
print(m.tan(45)) #1.6197751905438
print(m.tan(m.radians(45))) #0.9999999999999
#log()
print(m.log(1))#0